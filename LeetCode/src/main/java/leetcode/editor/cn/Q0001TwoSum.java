class Solution {
    public int[] twoSum(int[] nums, int target) {
        int[] res = new int[2];
        //key : target - nums[i], value = index
        Map<Integer,Integer> map = new HashMap<>();
        for (int i = 0; i < nums.length; i++) {
            Integer key = target - nums[i];
            if (null != map.get(key)){
                res[0] = i;
                res[1] = map.get(key);
            }else {
                map.put(nums[i],i);
            }
        }
        return res;
    }
}


