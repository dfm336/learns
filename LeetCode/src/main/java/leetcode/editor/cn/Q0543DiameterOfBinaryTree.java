class Solution {

    public int diameterOfBinaryTree(TreeNode root) {
        reverse(root);
        return max;
    }

    int max = 0;

    // è·å–å½“å‰ äºŒå‰æ ‘ çš„ æœ€å¤§ç›´å¾„
    //æ¯ä¸€é¢— äºŒå‰æ ‘çš„ã€Œç›´å¾„ã€é•¿åº¦ï¼Œå°±æ˜¯ä¸€ä¸ªèŠ‚ç‚¹çš„å·¦å³å­æ ‘çš„æœ€å¤§æ·±åº¦ä¹‹å’Œã€‚
    private int reverse(TreeNode root) {
        if ( root == null){
            return 0;
        }
        int leftDepth = reverse(root.left);
        int rightDepth = reverse(root.right);
        //ååº ä½ç½®
        if ( leftDepth + rightDepth > max ){
            max = leftDepth + rightDepth;
        }
        // æŸåˆ»å­æ ‘ çš„ æœ€å¤§ç›´å¾„  å°±æ˜¯ æœ€å¤§æ·±åº¦ã€‚ è¿™é‡Œè¦æŒ‰ç…§ è·å–æ·±åº¦ è¿”å›
        return 1 + Math.max(leftDepth,rightDepth);
    }

    public int maxDepth(TreeNode root){
        if (root == null){
            return 0;
        }
        return Math.max(maxDepth(root.left), maxDepth(root.right)) + 1;
    }
}
